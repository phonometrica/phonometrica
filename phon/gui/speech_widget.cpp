#include <phon/gui/speech_widget.hpp>

namespace phonometrica {

SpeechWidget::SpeechWidget(QWidget *parent) : QWidget(parent)
{

}

double SpeechWidget::xPosToTime(double x) const
{
    return window_start + (x * windowDuration() / width());
}

double SpeechWidget::timeToXPos(double t) const
{
    return (t - window_start) * width() / windowDuration();
}

} // namespace phonometrica